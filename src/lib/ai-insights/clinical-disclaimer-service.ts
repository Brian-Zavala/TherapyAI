/**
 * Middleware wrapper to check disclaimers before serving insights
 */
export async function withDisclaimerCheck<T>(
  userId: string,
  insightId: string,
  insightType: string,
  severity: 'low' | 'medium' | 'high',
  operation: () => Promise<T>
): Promise<{
  success: boolean;
  data?: T;
  disclaimer?: ClinicalDisclaimer;
  blockedReason?: string;
  requiredActions?: string[];
}> {
  const disclaimerService = getDisclaimerService();
  
  try {
    // Check if insight can be displayed
    const canDisplay = await disclaimerService.canDisplayInsight(
      userId,
      insightId,
      insightType,
      severity
    );

    if (!canDisplay.canDisplay) {
      const disclaimer = await disclaimerService.getRequiredDisclaimer(
        insightType, 
        severity
      );
      
      return {
        success: false,
        disclaimer,
        blockedReason: canDisplay.reason,
        requiredActions: canDisplay.requiredActions
      };
    }

    // Execute operation if checks pass
    const data = await operation();
    
    return {
      success: true,
      data
    };

  } catch (error) {
    logger.error('Disclaimer check failed', {
      userId,
      insightId,
      insightType,
      severity,
      error: error instanceof Error ? error.message : error
    });
    
    return {
      success: false,
      blockedReason: 'Safety check failed',
      requiredActions: ['Contact support']
    };
  }
}

