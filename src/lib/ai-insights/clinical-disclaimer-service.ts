  /**
   * Check if insight can be displayed to user
   */
  async canDisplayInsight(
    userId: string,
    insightId: string,
    insightType: string,
    severity: 'low' | 'medium' | 'high' = 'medium'
  ): Promise<{
    canDisplay: boolean;
    reason?: string;
    requiredActions?: string[];
  }> {
    try {
      // 1. Check therapist override requirement
      const requiresOverride = await this.requiresTherapistOverride(
        insightType, 
        0, // confidence doesn't matter - always require override
        severity
      );

      if (requiresOverride) {
        const override = await this.getTherapistOverride(userId, insightId);
        if (!override || override.overrideType !== 'approve') {
          return {
            canDisplay: false,
            reason: 'Requires therapist approval',
            requiredActions: ['Therapist must review and approve insight']
          };
        }

        // Check if override has expired
        if (override.expiresAt && new Date() > override.expiresAt) {
          return {
            canDisplay: false,
            reason: 'Therapist approval expired',
            requiredActions: ['Therapist must re-approve insight']
          };
        }
      }

      // 2. Check disclaimer acknowledgment
      const disclaimer = await this.getRequiredDisclaimer(insightType, severity);
      if (disclaimer.requiresAcknowledgment) {
        const acknowledged = await this.hasUserAcknowledgedDisclaimers(
          userId, 
          [disclaimer.id]
        );
        
        if (!acknowledged) {
          return {
            canDisplay: false,
            reason: 'Disclaimer not acknowledged',
            requiredActions: ['User must acknowledge clinical disclaimer']
          };
        }
      }

      return { canDisplay: true };

    } catch (error) {
      logger.error('Failed to check if insight can be displayed', {
        userId,
        insightId,
        insightType,
        severity,
        error: error instanceof Error ? error.message : error
      });
      
      // Fail secure - don't display if check fails
      return {
        canDisplay: false,
        reason: 'Safety check failed',
        requiredActions: ['System error - contact support']
      };
    }
  }

